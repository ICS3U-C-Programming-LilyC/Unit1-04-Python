def main():
    print("9 + 3 = {}".format(9 + 3))  # Addition
    print("203 - 74 = {}".format(203 - 74))  # Subtraction
    print("6*7 = {}".format(6 * 7))  # Multiplication
    print("77 / 2 = {}".format(77 / 2))  # Division
    print("25 / 3 = {}".format(25 / 3))  # Division with decimal answer
    print("11 + 8 * 4 = {}".format(11 + 8 * 4))  # Multiplication and addition
    print("9 - 2 / 2 = {}".format(9 - 2 / 2))  # Division and subtraction
    print("7^9 = {}".format(7 ^ 9))  # bitwise operator (not exponent)
    print("3**3 = {}".format(3**3))  # Exponent
    print("16**0.5 = {}".format(16**0.5))  # Square root


if __name__ == "__main__":
    main()
